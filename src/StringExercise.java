import java.util.Scanner;
public class StringExercise {
    public static void main(String[] args) {

        String dontEducation = "We don't need no education\n";
        String dontControl = "We don't need no thought control";

        System.out.println(dontEducation);
        System.out.println(dontControl);

        String check = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(check);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);




    }
}
